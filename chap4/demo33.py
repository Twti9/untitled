'''精度控制'''
print('%d'%99)
print('%10d'%99)#10表示宽度
print('%.3f'%3.1415926)#保留3位小数
print('%10.3f'%3.354675625)#字符占据10宽度，3位小数
print('%10s'%'hello')

print('{0}'.format(3.1415926))#0表示占位符的个数
print('{0:.3}'.format(3.141595456456))#.3表示数的个数
print('{0:.3f}'.format(3.141595456456))#.3f表示小数位数
print('{0:10.3f}'.format(3.141595456456))#10.3f表示字符串占据10宽度，保留3位小数
print('{0:10.3}'.format(3.141595456456))#10.3表示字符串占据10宽度，一共3个数