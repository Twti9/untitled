#lst.extend(lst2)依次把列表lst2中的元素添加到列表lst中
#lst.insert(1,90)在指定位置上添加元素
lst3=[True,False,'hello']
#切片操作，从指定位置添加N个元素
lst[1:2:]=lst3#表示从1切到2，不包括2上的元素，全部替换为lst3上的元素
print(lst)
