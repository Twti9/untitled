lst=[10,20,30,40]
print('添加前的列表元素',lst,id(lst))
lst.append(100)
print('添加后的列表元素',lst,id(lst))
lst2=['hello','world']
lst.append(45)
print(lst)
lst.extend(lst2)
lst.insert(2,98)
print(lst)
